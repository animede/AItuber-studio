async function startYouTubeComments() {
  if (!state.youtubeEnabled || !state.youtubeVideoId || !state.currentConversationId) {
    return;
  }

  hideError();
  try {
    if (state.youtubeConversationId && state.youtubeConversationId !== state.currentConversationId) {
      await stopYouTubeComments(state.youtubeConversationId);
    }
    const response = await fetch("/api/youtube/start", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        conversation_id: state.currentConversationId,
        video_id: state.youtubeVideoId,
      }),
    });
    const data = await parseResponseJson(response);
    state.youtubeRunning = true;
    state.youtubeConversationId = state.currentConversationId;
    state.youtubePollCursor = 0;
    state.youtubePendingComments = [];
    state.youtubeLastCommentKey = "";
    if (data.error) {
      setYouTubeStatus(`YouTube コメント取得エラー: ${data.error}`);
    } else {
      setYouTubeStatus(`YouTube コメント取得を開始中: ${data.video_id}`);
    }
    startYouTubePolling();
    await pollYouTubeComments();
  } catch (error) {
    state.youtubeRunning = false;
    stopYouTubePolling();
    setYouTubeStatus(error.message || "YouTube コメント取得を開始できませんでした。");
    showError(error.message || "YouTube コメント取得を開始できませんでした。");
  }
}

async function stopYouTubeComments(conversationId = state.youtubeConversationId || state.currentConversationId) {
  stopYouTubePolling();
  state.youtubePendingComments = [];
  state.youtubePollCursor = 0;
  state.youtubeLastCommentKey = "";
  if (!conversationId) {
    state.youtubeRunning = false;
    state.youtubeConversationId = null;
    setYouTubeStatus("YouTube コメント取得は停止中です。");
    return;
  }

  try {
    await fetch("/api/youtube/stop", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ conversation_id: conversationId }),
    });
  } catch (error) {
    console.warn("youtube stop", error);
  }

  state.youtubeRunning = false;
  state.youtubeConversationId = null;
  setYouTubeStatus("YouTube コメント取得は停止中です。");
}

async function pollYouTubeComments() {
  if (!state.youtubeRunning || !state.currentConversationId) {
    return;
  }

  try {
    const response = await fetch(`/api/youtube/comments/${encodeURIComponent(state.currentConversationId)}?since_seq=${state.youtubePollCursor}&limit=20`);
    const data = await parseResponseJson(response);
    state.youtubeRunning = Boolean(data.running);
    if (data.error) {
      setYouTubeStatus(`YouTube コメント取得エラー: ${data.error}`);
    } else if (state.youtubeRunning) {
      setYouTubeStatus(`YouTube コメント取得中: ${data.video_id}`);
    } else {
      setYouTubeStatus("YouTube コメント取得は停止中です。");
    }

    if (Array.isArray(data.comments) && data.comments.length > 0) {
      for (const item of data.comments) {
        const commentKey = item.comment_id || `${item.author_name}:${item.timestamp}:${item.message}`;
        if (commentKey === state.youtubeLastCommentKey) {
          continue;
        }
        state.youtubeLastCommentKey = commentKey;
        state.youtubePendingComments.push(item);
      }
      state.youtubePollCursor = Number(data.next_seq || state.youtubePollCursor);
      await drainYouTubePendingComments();
    }

    if (!state.youtubeRunning) {
      stopYouTubePolling();
    }
    syncYouTubeControls();
  } catch (error) {
    setYouTubeStatus(error.message || "YouTube コメント取得に失敗しました。");
  }
}

async function drainYouTubePendingComments() {
  if (!state.youtubeAutoSendEnabled || state.isStreaming || state.youtubePendingComments.length === 0) {
    return;
  }

  const nextComment = state.youtubePendingComments.shift();
  if (!nextComment) {
    return;
  }
  const authorName = nextComment.author_name || "viewer";
  await sendChatMessageText(`${authorName}: ${nextComment.message}`);
}

function renderTtsPanel() {
  const status = state.ttsStatus;
  const catalog = state.ttsCatalog;
  const summary = elements.ttsVoiceSummary;
  const voiceSelect = elements.ttsVoiceSelect;
  const voiceList = elements.ttsVoiceList;
  const modelList = elements.ttsModelList;
  const footerTtsVoice = elements.footerTtsVoice;
  if (!summary || !voiceSelect || !voiceList || !modelList) {
    return;
  }

  voiceSelect.innerHTML = "";
  voiceList.innerHTML = "";
  modelList.innerHTML = "";

  if (!catalog || catalog.available === false) {
    const reason = status?.error || catalog?.error || "TTS未接続";
    summary.textContent = reason;
    summary.classList.add("is-muted");
    if (footerTtsVoice) {
      footerTtsVoice.textContent = "TTS未接続";
    }
    voiceSelect.disabled = true;
    const option = document.createElement("option");
    option.value = "";
    option.textContent = "TTS未接続";
    voiceSelect.appendChild(option);
    appendListPlaceholder(voiceList, "利用できるボイスはまだ取得できていません。");
    appendListPlaceholder(modelList, "利用できるモデルはまだ取得できていません。");
    return;
  }

  summary.classList.remove("is-muted");
  voiceSelect.disabled = false;

  for (const speaker of catalog.speakers || []) {
    for (const style of speaker.styles || []) {
      const option = document.createElement("option");
      option.value = String(style.id);
      option.textContent = `${speaker.name} / ${style.name} (${style.id})`;
      if (style.id === state.selectedTtsStyleId) {
        option.selected = true;
      }
      voiceSelect.appendChild(option);
    }
  }

  const selectedVoice = getSelectedTtsVoice();
  if (selectedVoice) {
    summary.textContent = `${selectedVoice.speaker_name} / ${selectedVoice.style_name} (${catalog.version})`;
    if (footerTtsVoice) {
      footerTtsVoice.textContent = `${selectedVoice.speaker_name} / ${selectedVoice.style_name}`;
    }
  } else {
    summary.textContent = `style_id ${state.selectedTtsStyleId ?? catalog.default_style_id} / version ${catalog.version}`;
    if (footerTtsVoice) {
      footerTtsVoice.textContent = `style_id ${state.selectedTtsStyleId ?? catalog.default_style_id}`;
    }
  }

  const selectedStyleId = selectedVoice?.style_id;
  for (const speaker of catalog.speakers || []) {
    const styleNames = (speaker.styles || []).map((style) => `${style.name} (${style.id})`).join(", ");
    const hasSelectedStyle = (speaker.styles || []).some((style) => style.id === selectedStyleId);
    appendCatalogItem(
      voiceList,
      speaker.name,
      styleNames || "スタイル情報なし",
      hasSelectedStyle
    );
  }
  if (!voiceList.children.length) {
    appendListPlaceholder(voiceList, "話者一覧は空です。");
  }

  for (const model of catalog.models || []) {
    const suffix = model.is_default_model ? " / default" : "";
    appendCatalogItem(
      modelList,
      `${model.name}${suffix}`,
      `${model.speaker_count} speaker${model.speaker_count === 1 ? "" : "s"}`,
      Boolean(model.is_loaded)
    );
  }
  if (!modelList.children.length) {
    appendListPlaceholder(modelList, "モデル一覧は空です。");
  }
}

