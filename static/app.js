function inferVisualTypeFromPath(path, fallbackType = "image") {
  if (!path) {
    return fallbackType;
  }
  if (/\/api\/characters\/[^/]+\/assets\/(talking|waiting)(?:$|[?#/])/i.test(path)) {
    return "video";
  }
  if (/\.(mp4|webm|ogg|mov)$/i.test(path)) {
    return "video";
  }
  if (/\.(png|jpe?g|gif|webp|bmp|svg)$/i.test(path)) {
    return "image";
  }
  return fallbackType;
}

function inferVisualTypeFromFile(file, fallbackType = "image") {
  if (!file) {
    return fallbackType;
  }
  if (file.type.startsWith("video/")) {
    return "video";
  }
  if (file.type.startsWith("image/")) {
    return "image";
  }
  return inferVisualTypeFromPath(file.name || "", fallbackType);
}

