async function saveCharacterDefinition() {
  await maybeAutoGenerateCharacterId(true);
  if (!validateRoleNameForRegistration({ showWarning: true })) {
    return;
  }
  const draft = getCharacterDraft();
  const isCreate = state.characterEditorMode === "create";
  if (!draft.id) {
    showError("role に書かれた名前からキャラクタ登録名を自動生成できませんでした。");
    return;
  }
  if (!/^[a-z0-9][a-z0-9_-]*$/.test(draft.id)) {
    showError("登録名は英小文字、数字、ハイフン、アンダースコアで入力してください。");
    return;
  }
  if (!draft.role_text) {
    showError("キャラクタロールを入力してください。");
    return;
  }

  const formData = new FormData();
  formData.append("character_json", JSON.stringify(draft));
  if (elements.visualUploadInput.files[0]) {
    formData.append("visual_upload", elements.visualUploadInput.files[0]);
  }
  if (elements.talkingUploadInput.files[0]) {
    formData.append("talking_upload", elements.talkingUploadInput.files[0]);
  }
  if (elements.waitingUploadInput.files[0]) {
    formData.append("waiting_upload", elements.waitingUploadInput.files[0]);
  }

  hideError();
  try {
    const response = await fetch(
      isCreate ? "/api/characters" : `/api/characters/${encodeURIComponent(state.currentCharacterId)}`,
      {
        method: isCreate ? "POST" : "PUT",
        body: formData,
      }
    );
    const data = await parseResponseJson(response);
    await loadCharacters(data.character.id);
    if (isCreate) {
      await startConversation(data.character.id);
    } else {
      renderCharacterPanel();
    }
  } catch (error) {
    showError(error.message || "キャラクター保存に失敗しました。");
  }
}

