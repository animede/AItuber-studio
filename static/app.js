function resolveCharacterVisualAssets(character) {
  const mainPreviewPath = state.characterUploadPreviewUrls.visual_upload || character.visual_path || "";
  const mainPreviewType = state.characterUploadPreviewUrls.visual_upload
    ? inferVisualTypeFromFile(elements.visualUploadInput.files?.[0], character.visual_type || "image")
    : inferVisualTypeFromPath(mainPreviewPath, character.visual_type || "image");

  const talkingPreviewPath = state.characterUploadPreviewUrls.talking_upload || character.talking_visual_path || "";
  const talkingPreviewType = state.characterUploadPreviewUrls.talking_upload
    ? inferVisualTypeFromFile(elements.talkingUploadInput.files?.[0], mainPreviewType)
    : inferVisualTypeFromPath(talkingPreviewPath, mainPreviewType);

  const waitingPreviewPath = state.characterUploadPreviewUrls.waiting_upload || character.waiting_visual_path || "";
  const waitingPreviewType = state.characterUploadPreviewUrls.waiting_upload
    ? inferVisualTypeFromFile(elements.waitingUploadInput.files?.[0], mainPreviewType)
    : inferVisualTypeFromPath(waitingPreviewPath, mainPreviewType);

  return {
    main: {
      type: mainPreviewType,
      path: mainPreviewPath,
    },
    talking: {
      type: talkingPreviewPath ? talkingPreviewType : mainPreviewType,
      path: talkingPreviewPath || mainPreviewPath,
    },
    waiting: {
      type: waitingPreviewPath ? waitingPreviewType : mainPreviewType,
      path: waitingPreviewPath || mainPreviewPath,
    },
  };
}

function getCharacterVisualForMode(character, mode) {
  // talking / waiting 専用素材があれば優先し、無ければ初期素材へ戻す。
  const assets = resolveCharacterVisualAssets(character);

  if (mode === "talking" && assets.talking.path) {
    return assets.talking;
  }

  if (mode === "waiting" && assets.waiting.path) {
    return assets.waiting;
  }

  return assets.main;
}

