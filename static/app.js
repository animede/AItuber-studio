function extractCharacterName(roleValue, fallbackName) {
  // 明示的な「名前: ...」指定を最優先で採用する。
  const nameLineMatch = roleValue.match(/(?:^|\n)[ \t]*-?[ \t]*名前[ \t]*[:：][ \t]*([^\n\r]+)/);
  if (nameLineMatch) {
    return nameLineMatch[1].trim().replace(/^「(.+)」$/, "$1");
  }

  // 既存の role 文面からも、簡易的にキャラ名を拾えるようにしておく。
  const sentenceMatch = roleValue.match(/あなたは.*?「(.+?)」です/);
  if (sentenceMatch) {
    return sentenceMatch[1].trim();
  }

  return fallbackName;
}

function getRoleCharacterName() {
  return extractCharacterName(elements.roleText.value.trim(), "");
}

function syncCharacterNameFieldsFromRole() {
  if (state.characterEditorMode !== "create") {
    return;
  }

  const nextRoleName = getRoleCharacterName().trim();
  const previousRoleName = state.lastRoleDerivedCharacterName;
  const shouldReplace = (value) => !value || value === previousRoleName;

  if (!nextRoleName) {
    state.lastRoleDerivedCharacterName = "";
    return;
  }

  if (shouldReplace(elements.characterDisplayNameInput.value.trim())) {
    elements.characterDisplayNameInput.value = nextRoleName;
  }
  if (shouldReplace(elements.characterNameInput.value.trim())) {
    elements.characterNameInput.value = nextRoleName;
  }
  if (shouldReplace(elements.characterVoiceNameInput.value.trim())) {
    elements.characterVoiceNameInput.value = nextRoleName;
  }

  state.lastRoleDerivedCharacterName = nextRoleName;
}

