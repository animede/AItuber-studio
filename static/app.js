async function ensureCharacterLipsyncManifest(characterId) {
  if (!characterId) {
    return null;
  }

  const cached = getCharacterLipsyncManifest(characterId);
  if (cached) {
    return cached;
  }

  if (state.lipsyncManifestRequests[characterId]) {
    return state.lipsyncManifestRequests[characterId];
  }

  const request = fetch(`/api/characters/${encodeURIComponent(characterId)}/lipsync/manifest`)
    .then((response) => parseResponseJson(response))
    .then((manifest) => {
      const normalized = {
        ...manifest,
        available: Boolean(manifest.available),
        sprite_urls: manifest.sprite_urls || {},
        track: manifest.track || null,
      };
      state.lipsyncManifests[characterId] = normalized;
      delete state.lipsyncManifestRequests[characterId];

      if (state.currentCharacterId === characterId) {
        syncLipsyncAvailability();
        syncCharacterVisualMode();
        renderCharacterPanel();
      }
      return normalized;
    })
    .catch((error) => {
      delete state.lipsyncManifestRequests[characterId];
      console.warn("lipsync manifest load failed", characterId, error);
      return null;
    });

  state.lipsyncManifestRequests[characterId] = request;
  return request;
}

function getCurrentAudioLevel() {
  if (!state.audioAnalyser || !state.audioAnalyserData || !state.isAudioPlaying) {
    state.currentAudioLevel = 0;
    return 0;
  }

  state.audioAnalyser.getByteTimeDomainData(state.audioAnalyserData);
  let sum = 0;
  for (const sample of state.audioAnalyserData) {
    const normalized = (sample - 128) / 128;
    sum += normalized * normalized;
  }

  const rms = Math.sqrt(sum / state.audioAnalyserData.length);
  state.currentAudioLevel = Math.min(1, rms * 4.5);
  return state.currentAudioLevel;
}

function attachAudioAnalyser(audio) {
  const AudioContextConstructor = window.AudioContext || window.webkitAudioContext;
  if (!AudioContextConstructor || !audio) {
    return;
  }

  detachAudioAnalyser();

  try {
    if (!state.audioContext) {
      state.audioContext = new AudioContextConstructor();
    }
    if (state.audioContext.state === "suspended") {
      state.audioContext.resume().catch(() => {});
    }

    const analyser = state.audioContext.createAnalyser();
    analyser.fftSize = 512;
    analyser.smoothingTimeConstant = 0.45;

    const source = state.audioContext.createMediaElementSource(audio);
    source.connect(analyser);
    analyser.connect(state.audioContext.destination);

    state.audioAnalyser = analyser;
    state.audioAnalyserSource = source;
    state.audioAnalyserData = new Uint8Array(analyser.frequencyBinCount);
  } catch (error) {
    console.warn("audio analyser setup failed", error);
    detachAudioAnalyser();
  }
}

function detachAudioAnalyser() {
  if (state.audioAnalyserSource) {
    try {
      state.audioAnalyserSource.disconnect();
    } catch (error) {
      console.warn("audio analyser source disconnect failed", error);
    }
  }
  if (state.audioAnalyser) {
    try {
      state.audioAnalyser.disconnect();
    } catch (error) {
      console.warn("audio analyser disconnect failed", error);
    }
  }
  state.audioAnalyser = null;
  state.audioAnalyserSource = null;
  state.audioAnalyserData = null;
  state.currentAudioLevel = 0;
}

function getTrackFrameAtTime(track, currentTimeSeconds) {
  const frames = Array.isArray(track?.frames) ? track.frames : [];
  if (!frames.length) {
    return null;
  }

  const fps = Number(track?.fps) || 16;
  const frameIndex = Math.floor(Math.max(0, currentTimeSeconds) * fps) % frames.length;
  return frames[frameIndex] || null;
}

function getFrameQuadMetrics(frame, track) {
  if (!frame?.valid || !Array.isArray(frame.quad) || frame.quad.length !== 4) {
    return null;
  }

  const calibration = track?.calibration || null;
  const shouldApplyCalibration = Boolean(track?.calibrationApplied && calibration);
  const points = frame.quad.map(([x, y]) => {
    let nextX = Number(x);
    let nextY = Number(y);
    if (shouldApplyCalibration) {
      const scale = Number(calibration.scale) || 1;
      const rotation = ((Number(calibration.rotation) || 0) * Math.PI) / 180;
      const offsetX = Number(calibration.offset?.[0]) || 0;
      const offsetY = Number(calibration.offset?.[1]) || 0;
      const scaledX = nextX * scale;
      const scaledY = nextY * scale;
      nextX = scaledX * Math.cos(rotation) - scaledY * Math.sin(rotation) + offsetX;
      nextY = scaledX * Math.sin(rotation) + scaledY * Math.cos(rotation) + offsetY;
    }
    return { x: nextX, y: nextY };
  });

  const [topLeft, topRight, , bottomLeft] = points;
  const centerX = points.reduce((sum, point) => sum + point.x, 0) / points.length;
  const centerY = points.reduce((sum, point) => sum + point.y, 0) / points.length;
  const width = Math.hypot(topRight.x - topLeft.x, topRight.y - topLeft.y);
  const height = Math.hypot(bottomLeft.x - topLeft.x, bottomLeft.y - topLeft.y);
  const rotation = Math.atan2(topRight.y - topLeft.y, topRight.x - topLeft.x);

  return {
    centerX,
    centerY,
    width,
    height,
    rotationDegrees: (rotation * 180) / Math.PI,
  };
}

function pickLipsyncSpriteName(manifest, audioLevel) {
  const spriteUrls = manifest?.sprite_urls || {};
  if (!state.isAudioPlaying || audioLevel < 0.035) {
    return spriteUrls.closed ? "closed" : Object.keys(spriteUrls)[0] || "closed";
  }
  if (audioLevel >= 0.28 && spriteUrls.open) {
    return "open";
  }
  if (audioLevel >= 0.16 && spriteUrls.e) {
    return "e";
  }
  if (audioLevel >= 0.1 && spriteUrls.u) {
    return "u";
  }
  if (audioLevel >= 0.06 && spriteUrls.half) {
    return "half";
  }
  return spriteUrls.closed ? "closed" : Object.keys(spriteUrls)[0] || "closed";
}

